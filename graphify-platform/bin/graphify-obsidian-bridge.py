#!/usr/bin/env python3
"""
graphify-obsidian-bridge.py

Parses Graphify's graph.json and produces Obsidian-navigable wiki stubs:
  - _COMMUNITY_Community <N>.md   (one per community with >=MIN_NODES nodes)
  - _GRAPHIFY_INDEX.md            (master TOC, one per scan)

Designed to live next to GRAPH_REPORT.md inside <scan-target>/graphify-out/.
After running, all `[[_COMMUNITY_Community N]]` wiki-links in GRAPH_REPORT.md
resolve in Obsidian (since vault root contains the same path).

Usage:
  ./graphify-obsidian-bridge.py <path-to-graphify-out>
  ./graphify-obsidian-bridge.py src/graphify-out --min-nodes 4

Exit codes:
  0  ok
  1  graph.json missing or unreadable
  2  no communities passed threshold (degenerate graph)

Hard rules:
  - Reads only graph.json + GRAPH_REPORT.md (never source code).
  - Writes only `_COMMUNITY_*.md` and `_GRAPHIFY_INDEX.md` inside graphify-out/.
  - graphify-out/ is .gitignored — these stubs are regenerable, never committed.
  - Idempotent: re-runs overwrite cleanly.
"""

from __future__ import annotations

import argparse
import json
import sys
from collections import Counter, defaultdict
from datetime import datetime
from pathlib import Path

STUB_PREFIX = "_COMMUNITY_Community "
INDEX_NAME = "_GRAPHIFY_INDEX.md"


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Graphify→Obsidian wiki bridge")
    p.add_argument("graphify_out", type=Path, help="Path to a graphify-out/ directory")
    p.add_argument("--min-nodes", type=int, default=2,
                   help="Skip communities smaller than this (default: 2)")
    p.add_argument("--top-nodes", type=int, default=30,
                   help="Max nodes to list per community page (default: 30)")
    return p.parse_args()


def load_graph(graphify_out: Path) -> dict:
    fp = graphify_out / "graph.json"
    if not fp.is_file():
        sys.stderr.write(f"[bridge] FATAL: graph.json not found at {fp}\n")
        sys.exit(1)
    with fp.open("r", encoding="utf-8") as f:
        return json.load(f)


def degree_count(graph: dict) -> Counter:
    """Count edges incident to each node id."""
    deg: Counter[str] = Counter()
    for link in graph.get("links", []):
        s, t = link.get("_src"), link.get("_tgt")
        if s:
            deg[s] += 1
        if t and t != s:
            deg[t] += 1
    return deg


def cross_community_edges(graph: dict, node_to_comm: dict[str, int]) -> dict[int, Counter]:
    """For each community, count edges crossing into other communities."""
    bridges: dict[int, Counter] = defaultdict(Counter)
    for link in graph.get("links", []):
        s, t = link.get("_src"), link.get("_tgt")
        if not s or not t:
            continue
        cs, ct = node_to_comm.get(s), node_to_comm.get(t)
        if cs is None or ct is None or cs == ct:
            continue
        bridges[cs][ct] += 1
        bridges[ct][cs] += 1
    return bridges


def render_community(
    cid: int,
    nodes: list[dict],
    deg: Counter,
    bridges: Counter,
    top_nodes: int,
    repo_root: Path,
) -> str:
    """Render one community markdown page."""
    nodes_sorted = sorted(nodes, key=lambda n: deg[n["id"]], reverse=True)
    top = nodes_sorted[:top_nodes]
    rest_count = max(0, len(nodes_sorted) - top_nodes)

    lines: list[str] = [
        f"# Community {cid}",
        "",
        f"- **Size:** {len(nodes)} nodes",
        f"- **Top connectors:** see table below (sorted by degree)",
        "",
        "## Nodes",
        "",
        "| # | Node | Degree | File |",
        "|---|------|--------|------|",
    ]
    for i, n in enumerate(top, 1):
        label = n.get("label", n["id"])
        d = deg[n["id"]]
        src = n.get("source_file", "")
        loc = n.get("source_location", "")
        # Strip absolute prefix to keep table readable
        try:
            rel = str(Path(src).relative_to(repo_root))
        except (ValueError, TypeError):
            rel = src
        file_cell = f"`{rel}` · {loc}" if rel else ""
        lines.append(f"| {i} | `{label}` | {d} | {file_cell} |")
    if rest_count:
        lines.append(f"| … | _({rest_count} more not shown)_ | | |")

    if bridges:
        lines += ["", "## Cross-community bridges (edges leaving this community)", ""]
        for other_cid, count in bridges.most_common(8):
            lines.append(f"- → [[{STUB_PREFIX}{other_cid}|Community {other_cid}]] · **{count}** edges")

    lines += [
        "",
        "---",
        "",
        f"_Auto-generated by `graphify-obsidian-bridge.py` · {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}_  ",
        "_Back to: [[GRAPH_REPORT|Graph Report]]_",
        "",
    ]
    return "\n".join(lines)


def render_index(comm_sizes: dict[int, int], total_nodes: int, total_edges: int) -> str:
    """Render the master index file."""
    sorted_comms = sorted(comm_sizes.items(), key=lambda kv: -kv[1])
    lines = [
        "# Graphify Index",
        "",
        f"_Auto-generated · {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}_",
        "",
        f"- **Total nodes:** {total_nodes}",
        f"- **Total edges:** {total_edges}",
        f"- **Communities listed:** {len(sorted_comms)} (filtered)",
        "",
        "## Communities (by size)",
        "",
        "| Community | Nodes | Open |",
        "|-----------|-------|------|",
    ]
    for cid, size in sorted_comms:
        lines.append(f"| {cid} | {size} | [[{STUB_PREFIX}{cid}|→ open]] |")
    lines += ["", "Back to: [[GRAPH_REPORT|Graph Report]]", ""]
    return "\n".join(lines)


def main() -> int:
    args = parse_args()
    out_dir: Path = args.graphify_out.resolve()
    if not out_dir.is_dir():
        sys.stderr.write(f"[bridge] FATAL: {out_dir} is not a directory\n")
        return 1

    graph = load_graph(out_dir)
    nodes = graph.get("nodes", [])
    if not nodes:
        sys.stderr.write("[bridge] FATAL: graph has zero nodes\n")
        return 1

    # Repo root = git root if discoverable, else parent of graphify-out
    repo_root = out_dir.parent
    while repo_root != repo_root.parent and not (repo_root / ".git").exists():
        repo_root = repo_root.parent
    if not (repo_root / ".git").exists():
        repo_root = out_dir.parent

    # Bucket nodes by community
    by_comm: dict[int, list[dict]] = defaultdict(list)
    node_to_comm: dict[str, int] = {}
    for n in nodes:
        cid = n.get("community")
        if cid is None:
            continue
        by_comm[cid].append(n)
        node_to_comm[n["id"]] = cid

    deg = degree_count(graph)
    bridges = cross_community_edges(graph, node_to_comm)

    # Filter
    eligible = {cid: nodes for cid, nodes in by_comm.items() if len(nodes) >= args.min_nodes}
    if not eligible:
        sys.stderr.write(f"[bridge] no communities >= {args.min_nodes} nodes\n")
        return 2

    # Clean stale stubs (idempotency)
    removed = 0
    for old in out_dir.glob(f"{STUB_PREFIX}*.md"):
        try:
            old.unlink()
            removed += 1
        except OSError:
            pass

    # Write community stubs
    for cid, comm_nodes in eligible.items():
        body = render_community(
            cid, comm_nodes, deg, bridges.get(cid, Counter()),
            args.top_nodes, repo_root,
        )
        (out_dir / f"{STUB_PREFIX}{cid}.md").write_text(body, encoding="utf-8")

    # Write master index
    sizes = {cid: len(ns) for cid, ns in eligible.items()}
    (out_dir / INDEX_NAME).write_text(
        render_index(sizes, len(nodes), len(graph.get("links", []))),
        encoding="utf-8",
    )

    print(f"[bridge] OK · cleaned {removed} stale stubs · wrote {len(eligible)} community pages + {INDEX_NAME}")
    print(f"[bridge] dir: {out_dir}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
